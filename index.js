async function Withdraw(
  publicAddress,
  PrivateKey,
  walletAddress,
  amount
) {
  // we are getting the last transaction number for nonce
  const senderTransactionCount = await web3.eth.getTransactionCount(
    publicAddress
  );

  // get contract reference
  const contract = new web3.eth.Contract(ABI, ADDRESS);
  console.log("debug test usdc balance 1:", await contract.methods.balanceOf(walletAddress).call());

  // everything needs to be in hex
  const txObject = {
    nonce: web3.utils.toHex(senderTransactionCount),
    gasLimit: web3.utils.toHex("100000"),
    // gasPrice: web3.utils.toHex(web3.utils.toWei("250", 'gwei')),
    data: contract.methods.transfer(walletAddress, amount * 1000000).encodeABI(), // ABI encoded function that you need to run
    to: ADDRESS // transaction to your deployed contract
  }
console.log("debugg 1::");
  //-----------------sign the transaction

  const tx = new Transaction(txObject, { chain: 'goerli' })
  console.log("debugg 2::");
   tx.sign(Buffer.from(PrivateKey.toString(), 'hex'))
  const serializeTx = tx.serialize()
  const rawTx = '0x' + serializeTx.toString('hex')
  console.log("debugg 2::");

  //------------------broadcast the transaction

  const txSent = await web3.eth.sendSignedTransaction(rawTx)
  console.log("debug test usdc balance 2:", await contract.methods.balanceOf(walletAddress).call());

  return txSent;
 
  
}

